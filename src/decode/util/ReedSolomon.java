    public static int decodeBlock(byte[] block, boolean fix) {
        if (block == null || block.length != 255) return -1;

        byte[] synbuf = new byte[16];
        boolean errflag = false;

        // Syndrome: evaluate R(x) at roots α^1, α^2, ..., α^16
        // Note: 255-byte block has indices 0-254, so degree is 254
        for (int j = 0; j < 16; j++) {
            synbuf[j] = (byte) evaluate(block, 254, F256_POWER[j + 1] & 0xFF);
            if (synbuf[j] != 0) errflag = true;
        }

        if (!errflag) return 0;  // No errors detected
        if (!fix) return -1;      // Errors detected but not fixing

        byte[] sigma = new byte[POLY_LENGTH];
        byte[] omega = new byte[POLY_LENGTH];
        solveKeyEquation(synbuf, sigma, omega);
        return fixErrors(block, sigma, omega);
    }

    /**
     * Decode R2007 RS-encoded header data.
     *
     * @param data 765+ bytes of RS-encoded data (3 × 255-byte blocks)
     * @return 717 bytes of decoded data, or null on failure
     */
    public static byte[] decodeR2007Data(byte[] data) {
        if (data == null || data.length < 765) return null;
        try {
            byte[] block1 = new byte[255];
            byte[] block2 = new byte[255];
            byte[] block3 = new byte[255];
            System.arraycopy(data, 0, block1, 0, 255);
            System.arraycopy(data, 255, block2, 0, 255);
            System.arraycopy(data, 510, block3, 0, 255);

            decodeBlock(block1, true);
            decodeBlock(block2, true);
            decodeBlock(block3, true);

            byte[] result = new byte[717];
            System.arraycopy(block1, 0, result, 0, 239);
            System.arraycopy(block2, 0, result, 239, 239);
            System.arraycopy(block3, 0, result, 478, 239);
            return result;
        } catch (Exception e) {
            return null;
        }
    }

    private static int f256Multiply(int a, int b) {
        int prod = 0;
        int A = a;
        int B = b;
        while (B != 0) {
            if ((B & 1) != 0) prod ^= A;
            B >>= 1;
            A <<= 1;
        }
        prod ^= F256_RESIDUE[prod >> 8];
        return prod & 0xFF;
    }

    private static int evaluate(byte[] poly, int deg, int x) {
        // Horner's method: evaluate polynomial at point x
        // For deg < array length, this correctly handles the coefficients
        int y = 0;
        for (int i = Math.min(deg, poly.length - 1); i >= 0; i--) {
            y = f256Multiply(x, y) ^ (poly[i] & 0xFF);
        }
        return y;
    }

    private static int degree(byte[] poly) {
        int i = POLY_LENGTH - 1;
        while (i >= 0) {
            if (poly[i] != 0) break;
            i--;
        }
        return i;
    }

    private static void solveKeyEquation(byte[] s, byte[] sigma, byte[] omega) {
        byte[][][] matrix = new byte[2][3][POLY_LENGTH];
        matrix[0][0][0] = 1;
        System.arraycopy(s, 0, matrix[0][2], 0, 16);
        matrix[1][1][0] = 1;
        matrix[1][2][16] = 1;

        int fixedRow = 0;
        while (degree(matrix[fixedRow][0]) > 8 || degree(matrix[fixedRow][2]) > 7) {
            if (degree(matrix[0][2]) < degree(matrix[1][2])) {
                fixedRow = 0;
                rowOp(matrix, 1, 0);
            } else {
                fixedRow = 1;
                rowOp(matrix, 0, 1);
            }
        }

        System.arraycopy(matrix[fixedRow][0], 0, sigma, 0, POLY_LENGTH);
        System.arraycopy(matrix[fixedRow][2], 0, omega, 0, POLY_LENGTH);
    }

    private static void rowOp(byte[][][] matrix, int dst, int src) {
        int dstd = degree(matrix[dst][2]);
        int srcd = degree(matrix[src][2]);
        int power = dstd - srcd;

        int coeff = F256_INVERSE[matrix[src][2][srcd] & 0xFF];
        coeff = f256Multiply(coeff, matrix[dst][2][dstd] & 0xFF);

        for (int j = 0; j < 3; j++) {
            // MIN(17 - power, POLY_LENGTH) as in libredwg
            int limit = Math.min(17 - power, POLY_LENGTH);
            for (int i = 0; i < limit; i++) {
                int idx = i + power;
                // Safe boundary check: only operate within valid array range
                if (idx >= 0 && idx < POLY_LENGTH) {
                    matrix[dst][j][idx] ^= (byte) f256Multiply(coeff, matrix[src][j][i] & 0xFF);
                }
            }
        }
    }

    private static int fixErrors(byte[] blk, byte[] sigma, byte[] omega) {
        int nerr = 0;
        int[] roots = new int[8];

        for (int x = 0; x < 256; x++) {
            if (evaluate(sigma, 8, x) == 0) {
                if (nerr >= 8) return -1;
                roots[nerr++] = x;
            }
        }

        int d = degree(sigma);
        if (nerr != d) return -1;

        for (int i = 0; i < 8; i++) {
            sigma[2 * i] = sigma[2 * i + 1];
            sigma[2 * i + 1] = 0;
        }

        for (int i = 0; i < nerr; i++) {
            int sigmaval = evaluate(sigma, 16, roots[i]);
            int omegaval = evaluate(omega, 16, roots[i]);
            int chaff = F256_INVERSE[sigmaval];
            int errorval = f256Multiply(chaff, omegaval);
            int pos = F256_LOGARITHM[F256_INVERSE[roots[i]]];
            blk[pos] ^= (byte) errorval;
        }

        return nerr;
    }
