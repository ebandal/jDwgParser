    // 생성 다항식 계수 (degree 16: 17개 계수)
    private static final int[] RSGEN = {
        0x6a, 0xe3, 0x63, 0x1f, 0xa1, 0x24, 0x9e, 0x44, 0x13,
        0x1e, 0x2f, 0xfc, 0xfd, 0xce, 0xa9, 0xdb, 0x01,
    };

    private static final int PARITY_LENGTH = 16;
    private static final int DATA_LENGTH = 239;
    private static final int CODE_LENGTH = 255;

    /**
     * 단일 블록 인코딩: 239바이트 → 255바이트 (239 + 16 패리티)
     */
    public static byte[] encodeBlock(byte[] data) {
        if (data.length != DATA_LENGTH) {
            throw new IllegalArgumentException("Expected " + DATA_LENGTH + " bytes, got " + data.length);
        }

        // 결과: 239 데이터바이트 + 16 패리티 바이트
        byte[] codeword = new byte[CODE_LENGTH];
        System.arraycopy(data, 0, codeword, 0, DATA_LENGTH);

        // Systematic RS encoding: data[0..238] | parity[0..15]
        // Compute remainder of (data * x^16) / generator
        byte[] remainder = computeRemainder(data);

        // 마지막 16바이트에 패리티 저장
        System.arraycopy(remainder, 0, codeword, DATA_LENGTH, PARITY_LENGTH);

        return codeword;
    }

    /**
     * 3개 블록 인코딩: 717바이트(3x239) → 765바이트(3x255)
     * R2007 파일 헤더 보호용
     */
    public static byte[] encodeR2007Data(byte[] data) {
        if (data.length != 717) {
            throw new IllegalArgumentException("Expected 717 bytes, got " + data.length);
        }

        byte[] encoded = new byte[765];

        // Block 0: bytes 0-238
        byte[] block0 = new byte[DATA_LENGTH];
        System.arraycopy(data, 0, block0, 0, DATA_LENGTH);
        byte[] enc0 = encodeBlock(block0);
        System.arraycopy(enc0, 0, encoded, 0, CODE_LENGTH);

        // Block 1: bytes 239-477
        byte[] block1 = new byte[DATA_LENGTH];
        System.arraycopy(data, DATA_LENGTH, block1, 0, DATA_LENGTH);
        byte[] enc1 = encodeBlock(block1);
        System.arraycopy(enc1, 0, encoded, CODE_LENGTH, CODE_LENGTH);

        // Block 2: bytes 478-716
        byte[] block2 = new byte[DATA_LENGTH];
        System.arraycopy(data, DATA_LENGTH * 2, block2, 0, DATA_LENGTH);
        byte[] enc2 = encodeBlock(block2);
        System.arraycopy(enc2, 0, encoded, CODE_LENGTH * 2, CODE_LENGTH);

        return encoded;
    }

    /**
     * Compute remainder: polynomial division of (data * x^16) by generator polynomial
     * Used to derive parity bytes for systematic RS encoding
     */
    private static byte[] computeRemainder(byte[] data) {
        // Buffer to hold [data | zeros] for division
        byte[] temp = new byte[CODE_LENGTH];
        System.arraycopy(data, 0, temp, 0, DATA_LENGTH);
        // temp[DATA_LENGTH..254] are zeros (parity placeholders)

        // Polynomial long division: divide by generator polynomial
        // Generator has degree 16, so RSGEN has 17 coefficients
        for (int i = 0; i < DATA_LENGTH; i++) {
            int coeff = temp[i];
            if (coeff == 0) continue;

            // Multiply entire generator by this coefficient and subtract (XOR in GF)
            for (int j = 0; j < RSGEN.length; j++) {
                int product = gfMultiply(coeff, RSGEN[j]);
                temp[i + j] ^= product;
            }
        }

        // Remainder is the last 16 bytes (parity)
        byte[] remainder = new byte[PARITY_LENGTH];
        System.arraycopy(temp, DATA_LENGTH, remainder, 0, PARITY_LENGTH);
        return remainder;
    }

    /**
     * GF(256) multiplication using log tables
     */
    private static int gfMultiply(int a, int b) {
        if (a == 0 || b == 0) return 0;

        // log(a*b) = log(a) + log(b) mod 255
        int logSum = F256_LOGARITHM[a & 0xFF] + F256_LOGARITHM[b & 0xFF];
        return F256_POWER[logSum % 255] & 0xFF;
    }
