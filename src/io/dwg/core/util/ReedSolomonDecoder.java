    /**
     * GF(256) 곱셈
     */
    private static int f256Multiply(int a, int b) {
        int prod = 0;
        int A = a;
        int B = b;

        while (B != 0) {
            if ((B & 1) != 0) {
                prod ^= A;
            }
            B >>= 1;
            A <<= 1;
        }

        prod ^= F256_RESIDUE[prod >> 8];
        return prod & 0xFF;
    }

    /**
     * 다항식 평가 (Horner's method)
     */
    private static int evaluate(byte[] poly, int deg, int x) {
        int y = poly[deg] & 0xFF;
        while (deg >= 0) {
            y = f256Multiply(x, y) ^ (poly[deg] & 0xFF);
            deg--;
        }
        return y;
    }

    /**
     * 다항식의 차수를 구합니다.
     */
    private static int degree(byte[] poly) {
        int i = POLY_LENGTH - 1;
        while (i >= 0) {
            if (poly[i] != 0) {
                break;
            }
            i--;
        }
        return i;
    }

    /**
     * Berlekamp-Massey 알고리즘으로 에러 로케이터 다항식 계산
     */
    private static void solveKeyEquation(byte[] s, byte[] sigma, byte[] omega) {
        // 행렬 초기화
        byte[][][] matrix = new byte[2][3][POLY_LENGTH];

        // matrix[0][0] = 1
        matrix[0][0][0] = 1;

        // matrix[0][2] = s (신드롬)
        System.arraycopy(s, 0, matrix[0][2], 0, 16);

        // matrix[1][1] = 1
        matrix[1][1][0] = 1;

        // matrix[1][2][16] = 1
        matrix[1][2][16] = 1;

        // Gaussian elimination (행 소거)
        int fixedRow = 0;
        while (degree(matrix[fixedRow][0]) > 8 ||
               degree(matrix[fixedRow][2]) > 7) {
            if (degree(matrix[0][2]) < degree(matrix[1][2])) {
                fixedRow = 0;
                rowOp(matrix, 1, 0);
            } else {
                fixedRow = 1;
                rowOp(matrix, 0, 1);
            }
        }

        // 결과 추출
        System.arraycopy(matrix[fixedRow][0], 0, sigma, 0, POLY_LENGTH);
        System.arraycopy(matrix[fixedRow][2], 0, omega, 0, POLY_LENGTH);
    }

    /**
     * 행 연산 (row operation)
     */
    private static void rowOp(byte[][][] matrix, int dst, int src) {
        int dstd = degree(matrix[dst][2]);
        int srcd = degree(matrix[src][2]);
        int power = dstd - srcd;

        int coeff = F256_INVERSE[matrix[src][2][srcd] & 0xFF];
        coeff = f256Multiply(coeff, matrix[dst][2][dstd] & 0xFF);

        // 행 연산 수행
        for (int j = 0; j < 3; j++) {
            int limit = Math.min(17 - power, POLY_LENGTH);
            for (int i = 0; i < limit; i++) {
                int idx = i + power;
                if (idx >= 0 && idx < POLY_LENGTH) {
                    matrix[dst][j][idx] ^= (byte) f256Multiply(
                        coeff,
                        matrix[src][j][i] & 0xFF
                    );
                }
            }
        }
    }

    /**
     * 에러 위치와 값 계산 (Chien search + Forney algorithm)
     */
    private static int fixErrors(byte[] blk, byte[] sigma, byte[] omega) {
        int nerr = 0;
        int[] roots = new int[8];

        // Chien search: 에러 위치 찾기
        for (int x = 0; x < 256; x++) {
            if (evaluate(sigma, 8, x) == 0) {
                roots[nerr++] = x;
            }
        }

        // 에러 개수 확인
        int d = degree(sigma);
        if (nerr != d) {
            return -1; // 복구 불가
        }

        // 시그마 미분 (실제로는 짝수 인덱스만 사용)
        for (int i = 0; i < 8; i++) {
            sigma[2 * i] = sigma[2 * i + 1];
            sigma[2 * i + 1] = 0;
        }

        // Forney 알고리즘: 에러값 계산 및 수정
        for (int i = 0; i < nerr; i++) {
            int sigmaval = evaluate(sigma, 16, roots[i]);
            int omegaval = evaluate(omega, 16, roots[i]);

            int chaff = F256_INVERSE[sigmaval];
            int errorval = f256Multiply(chaff, omegaval);
            int pos = F256_LOGARITHM[F256_INVERSE[roots[i]]];

            blk[pos] ^= (byte) errorval;
        }

        return nerr;
    }
