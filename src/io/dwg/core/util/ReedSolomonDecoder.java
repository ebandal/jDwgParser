    /**
     * 255바이트 블록을 복호화합니다.
     * 최대 8바이트의 에러를 수정할 수 있습니다.
     *
     * @param block 255바이트의 RS 인코딩된 데이터 (수정 대상)
     * @param fix   true이면 에러 수정, false이면 에러만 감지
     * @return 수정된 에러 개수 (0=에러없음, >0=수정됨, -1=복구불가)
     */
    public static int decodeBlock(byte[] block, boolean fix) {
        if (block == null || block.length != 255) {
            return -1;
        }

        byte[] synbuf = new byte[16];
        boolean errflag = false;

        // 신드롬 계산 (syndrome = evaluate(block, 254, f256_power[j+1]))
        // 블록 길이가 255이므로 최대 인덱스는 254
        for (int j = 0; j < 16; j++) {
            synbuf[j] = (byte) evaluate(block, 254, F256_POWER[j + 1] & 0xFF);
            if (synbuf[j] != 0) {
                errflag = true;
            }
        }

        // 에러가 없으면 반환
        if (!errflag) {
            return 0;
        }

        // 에러가 있지만 수정 불가능
        if (!fix) {
            return -1;
        }

        // 에러 로케이터 다항식 계산
        byte[] sigma = new byte[POLY_LENGTH];
        byte[] omega = new byte[POLY_LENGTH];
        solveKeyEquation(synbuf, sigma, omega);

        // 에러 수정
        return fixErrors(block, sigma, omega);
    }

