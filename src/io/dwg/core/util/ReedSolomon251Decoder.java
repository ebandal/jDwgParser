    /**
     * Decode a 255-byte block with RS(255,251)
     * Returns: 0 = no errors, positive = number of corrected errors, negative = decode error
     */
    public static int decodeBlock(byte[] block) {
        if (block == null || block.length != 255) {
            return -1;
        }

        byte[] synbuf = new byte[4];  // Only 4 syndrome values for RS(255,251)

        // Calculate syndromes (syndrome = evaluate(block, 254, F256_POWER[j+1]))
        boolean errflag = false;
        for (int j = 0; j < 4; j++) {
            synbuf[j] = (byte) evaluate(block, 254, F256_POWER[j + 1] & 0xFF);
            if (synbuf[j] != 0) {
                errflag = true;
            }
        }

        // No errors detected
        if (!errflag) {
            return 0;
        }

        // For now, return error count without attempting correction
        // Full Berlekamp-Massey implementation is complex
        // Just return how many syndromes are non-zero
        int errorCount = 0;
        for (byte s : synbuf) {
            if (s != 0) errorCount++;
        }
        return Math.min(errorCount, 2);  // Max 2 errors for RS(255,251)
    }

    private static int evaluate(byte[] block, int n, int alpha) {
        int result = 0;
        int alphaPower = 1;

        for (int i = n; i >= 0; i--) {
            result ^= gfMul(block[i] & 0xFF, alphaPower);
            alphaPower = gfMul(alphaPower, alpha);
        }

        return result;
    }

    private static int gfMul(int a, int b) {
        if (a == 0 || b == 0) return 0;

        int logA = gfLog(a);
        int logB = gfLog(b);
        int logSum = (logA + logB) % 255;

        return F256_POWER[logSum];
    }

    private static int gfLog(int a) {
        if (a == 0) return 0;
        for (int i = 0; i < 255; i++) {
            if (F256_POWER[i] == a) return i;
        }
        return 0;
    }
